#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	unsigned int i;

	if (s == NULL)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
